const mongoose = require("mongoose");

const empSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    role: {
      type: String,
      required: true,
    },
    date: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const EmpData = mongoose.model("EmpData", empSchema);

module.exports = { EmpData };
